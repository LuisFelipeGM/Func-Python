def soma(n1, n2):
    '''
    Retorna a soma de dois números.

    Parâmetros
    ----------
    n1, n2 : int ou float
    Descrição: números a serem somados.

    Retorno
    ----------
    retorna um valor int ou float.
    Descrição: retorna a soma de n1 e n2.
    '''
    s = n1 + n2
    return s
